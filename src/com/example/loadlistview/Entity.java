package com.example.loadlistview;

public class Entity {
	private String name;
	private String des;
	private String info;
	protected String getName() {
		return name;
	}
	protected void setName(String name) {
		this.name = name;
	}
	protected String getDes() {
		return des;
	}
	protected void setDes(String des) {
		this.des = des;
	}
	protected String getInfo() {
		return info;
	}
	protected void setInfo(String info) {
		this.info = info;
	}
	
	

}
